# 1. assignment operator
a = 10
print(a)

# 2. unary operator
b = 5
print(-b)

# 3. relational operator
c = 10
d = 20
print(c < d)
print(c==d)

# 4. logical operator
x = True
y = False
print(x and y)
print(x or y)
print(not x)

# 5. boolean operator
is_pass = True
print(is_pass)

# 6. bitwise operator
a = 5
b = 3
print (a&b) # AND
print (a|b) # OR

# 7. membership 
numbers = [1,2,3,4,5]
print(2 in numbers)
print(5 not in numbers)

# 8. identity operator
x = 10
y = 10
print(x is y)
print(x is not y)